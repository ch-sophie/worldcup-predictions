import streamlit as st
import pandas as pd
from supabase import create_client, Client
import os
from dotenv import load_dotenv

# Load environment variables
load_dotenv()

st.set_page_config(page_title="2026 World Cup ML Portal", page_icon="🏆", layout="wide")

# --- Supabase Initialization ---
SUPABASE_URL = os.getenv("SUPABASE_URL")
SUPABASE_KEY = os.getenv("SUPABASE_KEY")

if not SUPABASE_URL or not SUPABASE_KEY:
    st.error("⚠️ Missing Supabase credentials! Please verify SUPABASE_URL and SUPABASE_KEY in your .env file.")
    st.stop()

# Initialize API client
supabase: Client = create_client(SUPABASE_URL, SUPABASE_KEY)

# ────────────────────────────────────────────────────────────
# DATA FETCHING CACHE (Pulling purely from your Supabase tables)
# ────────────────────────────────────────────────────────────

@st.cache_data(ttl=60)
def fetch_live_matches():
    """Fetches completed matches from Supabase and filters strictly for the 2026 FIFA World Cup."""
    try:
        response = supabase.table("fixtures_2026_live").select("*").execute()
        df = pd.DataFrame(response.data)
        if not df.empty:
            df['date']  = pd.to_datetime(df['date'], errors='coerce')
            df['team1'] = df['team1'].astype(str).str.title()
            df['team2'] = df['team2'].astype(str).str.title()
            
            # CRITICAL FILTER: Show ONLY 2026 FIFA World Cup matches from your results file split
            df['tournament_clean'] = df['tournament'].fillna('').astype(str).str.lower()
            df = df[
                (df['tournament_clean'].str.contains('fifa world cup')) & 
                (df['date'].dt.year == 2026)
            ]
            df = df.drop(columns=['tournament_clean'])
            
        return df
    except Exception as e:
        st.error(f"Error reading fixtures_2026_live from Supabase: {e}")
        return pd.DataFrame()

@st.cache_data(ttl=300)
def fetch_upcoming_fixtures():
    """Fetches upcoming schedule from the Supabase upcoming table."""
    try:
        response = supabase.table("fixtures_2026_upcoming").select("*").execute()
        df = pd.DataFrame(response.data)
        if not df.empty:
            df['team1'] = df['team1'].astype(str).str.title()
            df['team2'] = df['team2'].astype(str).str.title()
            df['date']  = pd.to_datetime(df['date'], errors='coerce')
        return df
    except Exception as e:
        st.error(f"Error reading fixtures_2026_upcoming from Supabase: {e}")
        return pd.DataFrame()

@st.cache_data(ttl=120)
def fetch_match_predictions():
    """Fetches the match-by-match prediction odds generated by your ML script."""
    try:
        response = supabase.table("predictions_2026").select("*").execute()
        df = pd.DataFrame(response.data)
        if not df.empty:
            df['team1']      = df['team1'].astype(str).str.title()
            df['team2']      = df['team2'].astype(str).str.title()
            df['prediction'] = df['prediction'].astype(str).str.title()
            df['date']       = pd.to_datetime(df['date'], errors='coerce')
        return df
    except Exception as e:
        st.error(f"Error reading predictions_2026 from Supabase: {e}")
        return pd.DataFrame()

@st.cache_data(ttl=60)
def fetch_tournament_simulation():
    """Fetches Monte Carlo overall winner rankings from your simulation."""
    try:
        response = supabase.table("tournament_predictions").select("*").execute()
        df = pd.DataFrame(response.data)
        if not df.empty and 'win_pct' in df.columns:
            df = df.sort_values('win_pct', ascending=False).reset_index(drop=True)
        return df
    except Exception as e:
        st.error(f"Error reading tournament_predictions from Supabase: {e}")
        return pd.DataFrame()

# --- Initial Data Load ---
df_live_raw  = fetch_live_matches()
df_up_raw    = fetch_upcoming_fixtures()
df_preds_raw = fetch_match_predictions()
df_tourney   = fetch_tournament_simulation()

# ────────────────────────────────────────────────────────────
# SIDEBAR FILTERS
# ────────────────────────────────────────────────────────────
st.sidebar.header("🔍 Global Dashboard Filters")

# Harvest unique countries present across your data pools
all_countries = set()
for frame in [df_live_raw, df_up_raw, df_preds_raw]:
    if not frame.empty:
        if 'team1' in frame.columns: all_countries.update(frame['team1'].dropna())
        if 'team2' in frame.columns: all_countries.update(frame['team2'].dropna())

selected_teams = st.sidebar.multiselect(
    "Filter by Country:", 
    sorted(list(all_countries)), 
    placeholder="Showing All Countries"
)

# Apply global master filters safely 
def run_filter_pipeline(df):
    if df.empty: 
        return df
    mutated_df = df.copy()
    if selected_teams:
        mutated_df = mutated_df[
            mutated_df['team1'].isin(selected_teams) | 
            mutated_df['team2'].isin(selected_teams)
        ]
    return mutated_df

df_live  = run_filter_pipeline(df_live_raw)
df_up    = run_filter_pipeline(df_up_raw)
df_preds = run_filter_pipeline(df_preds_raw)

# ────────────────────────────────────────────────────────────
# MAIN VIEW INTERFACE
# ────────────────────────────────────────────────────────────
st.title("🏆 FIFA World Cup 2026 Analytics Portal")
st.markdown("---")

tab1, tab2, tab3, tab4 = st.tabs([
    "🔴 Completed Matches", "🗓️ Upcoming Schedule", "🤖 Machine Learning Predictions", "👑 Tournament Simulation Favourites"
])

# TAB 1: Completed Matches (Strictly 2026 World Cup)
with tab1:
    st.subheader("Completed 2026 World Cup Matches")
    if df_live.empty:
        st.info("No completed 2026 FIFA World Cup matches found yet. Real-time entries will appear here as updates pass through your pipeline.")
    else:
        for _, match in df_live.iterrows():
            g1 = int(match.get('score1', 0) if pd.notnull(match.get('score1')) else 0)
            g2 = int(match.get('score2', 0) if pd.notnull(match.get('score2')) else 0)
            
            c1 = "#28a745" if g1 > g2 else "#dc3545" if g1 < g2 else "#6c757d"
            c2 = "#28a745" if g2 > g1 else "#dc3545" if g2 < g1 else "#6c757d"
            
            with st.container(border=True):
                col_t1, col_s1, col_s2, col_t2 = st.columns([3, 1, 1, 3])
                with col_t1: st.markdown(f"### {match.get('team1', 'Unknown')}")
                with col_s1: st.markdown(f"<h2 style='color:{c1}; text-align:center;'>{g1}</h2>", unsafe_allow_html=True)
                with col_s2: st.markdown(f"<h2 style='color:{c2}; text-align:center;'>{g2}</h2>", unsafe_allow_html=True)
                with col_t2: st.markdown(f"### {match.get('team2', 'Unknown')}")
                
                match_date = match['date'].strftime('%B %d, %Y') if pd.notnull(match.get('date')) else "Date Missing"
                st.caption(f"📅 **{match_date}** | 📍 {str(match.get('city', 'Unknown')).title()}, {str(match.get('country', 'Unknown')).title()} | Stage: `{str(match.get('stage', 'Unknown')).upper()}`")

# TAB 2: Upcoming Fixtures List
with tab2:
    st.subheader("Official Slated Fixture Grid")
    if df_up.empty:
        st.info("No upcoming entries matching your filters found in `fixtures_2026_upcoming`.")
    else:
        grid_display = df_up.copy()
        if 'date' in grid_display.columns:
            grid_display['match_date'] = grid_display['date'].dt.strftime('%b %d, %Y')
            grid_display = grid_display.drop(columns=['date'])
            
        columns_to_show = [c for c in ['match_date', 'team1', 'team2', 'stage', 'tournament', 'city', 'country'] if c in grid_display.columns]
        st.dataframe(grid_display[columns_to_show], use_container_width=True, hide_index=True)

# TAB 3: Match Win Probabilities 
with tab3:
    st.subheader("🤖 Algorithmic Match Win Probabilities")
    if df_preds.empty:
        st.info("No current data found in `predictions_2026`. Run `predict_kaggle.py` to calculate predictions.")
    else:
        for _, pred_row in df_preds.iterrows():
            p1  = float(pred_row.get('prob_team1_win', 33.3))
            pdr = float(pred_row.get('prob_draw', 33.3))
            p2  = float(pred_row.get('prob_team2_win', 33.3))
            chosen_pick = pred_row.get('prediction', 'Tie')
            
            with st.container(border=True):
                raw_d = pred_row.get('date')
                display_d = pd.to_datetime(raw_d).strftime('%b %d, %Y') if pd.notnull(raw_d) else "Tournament Schedule"
                stg = str(pred_row.get('stage','')).replace('_',' ').title()
                st.caption(f"🏆 {stg} • Match Day: {display_d}")
                
                c1, c2, c3, c4, c5 = st.columns([3, 2, 1, 2, 3])
                with c1:
                    st.markdown(f"### {pred_row.get('team1')}")
                    st.metric("Win Probability", f"{p1:.1f}%")
                with c2:
                    st.markdown("<br>", unsafe_allow_html=True)
                    st.progress(max(0, min(int(p1), 100)))
                with c3:
                    st.markdown(f"<h3 style='text-align:center; margin-top:5px;'>VS</h3>"
                                f"<p style='text-align:center; color:#777; font-size: 0.9em;'>Draw<br><b>{pdr:.1f}%</b></p>", 
                                unsafe_allow_html=True)
                with c4:
                    st.markdown("<br>", unsafe_allow_html=True)
                    st.progress(max(0, min(int(p2), 100)))
                with c5:
                    st.markdown(f"### {pred_row.get('team2')}")
                    st.metric("Win Probability", f"{p2:.1f}%")
                
                st.markdown(f"🔮 **Model Predicted Outcome:** `{chosen_pick}`")

# TAB 4: Tournament Winner Probabilities
with tab4:
    st.subheader("👑 Simulation Championship Forecasts")
    if df_tourney.empty:
        st.info("No simulation records found in `tournament_predictions`. Run `predict_kaggle.py --tournament` to generate them.")
    else:
        layout_left, layout_right = st.columns([2, 3])
        
        with layout_left:
            st.markdown("#### 🥇 Current Top 5 Favourites")
            medal_icons = ["🥇", "🥈", "🥉", "4️⃣", "5️⃣"]
            for index, row in df_tourney.head(5).reset_index(drop=True).iterrows():
                st.metric(
                    label=f"{medal_icons[index]} {str(row['team']).title()}",
                    value=f"{float(row['win_pct']):.2f}%",
                    delta=f"Reaches Finals: {float(row.get('final_pct', 0)):.1f}%"
                )
                
        with layout_right:
            st.markdown("#### 🌍 Full Simulation Board")
            display_tourney = df_tourney.copy()
            if 'team' in display_tourney.columns:
                display_tourney['team'] = display_tourney['team'].str.title()
                
            cols_to_display = [c for c in ['rank', 'team', 'win_pct', 'final_pct', 'semifinal_pct', 'win_count'] if c in display_tourney.columns]
            st.dataframe(display_tourney[cols_to_display], use_container_width=True, hide_index=True)